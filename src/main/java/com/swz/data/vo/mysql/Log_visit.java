package com.swz.data.vo.mysql;

import java.util.Date;

public class Log_visit{
	private Integer visit_id;
	private Date visitlog_date;
	private String visit_message;
	private Integer question_id;
	private Integer DTMF;
	private String tel_event;

	public void setVisit_id(Integer visit_id){
		this.visit_id = visit_id;
	}
	public Integer getVisit_id(){
		return visit_id;
	}
	public void setVisitlog_date(Date visitlog_date){
		this.visitlog_date = visitlog_date;
	}
	public Date getVisitlog_date(){
		return visitlog_date;
	}
	public void setVisit_message(String visit_message){
		this.visit_message = visit_message;
	}
	public String getVisit_message(){
		return visit_message;
	}
	public void setQuestion_id(Integer question_id){
		this.question_id = question_id;
	}
	public Integer getQuestion_id(){
		return question_id;
	}
	public void setDTMF(Integer DTMF){
		this.DTMF = DTMF;
	}
	public Integer getDTMF(){
		return DTMF;
	}
	public void setTel_event(String tel_event){
		this.tel_event = tel_event;
	}
	public String getTel_event(){
		return tel_event;
	}

	@Override	
	public String toString() {	
		return "{"+"\"visit_id\":\""+this.visit_id+"\""+",\"visitlog_date\":\""+this.visitlog_date+"\""+",\"visit_message\":\""+this.visit_message+"\""+",\"question_id\":\""+this.question_id+"\""+",\"DTMF\":\""+this.DTMF+"\""+",\"tel_event\":\""+this.tel_event+"\""+"}";	}
}

